import { promises as fs } from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();
const imagesDir = path.join(repoRoot, 'src', 'public', 'assets', 'images');
const outputFile = path.join(repoRoot, 'src', 'app', 'context', 'localUploadedImages.ts');

const allowedExts = new Set(['.jpg', '.jpeg', '.png', '.webp']);

const toPosixPath = (p) => p.split(path.sep).join('/');

async function main() {
  let entries;
  try {
    entries = await fs.readdir(imagesDir, { withFileTypes: true });
  } catch (err) {
    console.error(`[generate-public-image-manifest] Could not read: ${imagesDir}`);
    console.error(err);
    process.exit(1);
  }

  const files = entries
    .filter((e) => e.isFile())
    .map((e) => e.name)
    .filter((name) => allowedExts.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b));

  const versions = {};
  await Promise.all(
    files.map(async (file) => {
      const stat = await fs.stat(path.join(imagesDir, file));
      // Cache-busting version for when a file is replaced but keeps the same name.
      // Keep it stable and serializable.
      versions[file] = `${stat.size}-${Math.floor(stat.mtimeMs)}`;
    })
  );

  const relImagesDir = toPosixPath(path.relative(repoRoot, imagesDir));
  const banner = `// This file is auto-generated by scripts/generate-public-image-manifest.mjs\n// Source folder: ${relImagesDir}\n// Do not edit manually.\n`;

  const content =
    banner +
    `\nexport const LOCAL_UPLOADED_IMAGE_BASE_PATH = '/assets/images/';\n` +
    `export const LOCAL_UPLOADED_IMAGE_FILES = ${JSON.stringify(files, null, 2)} as const;\n` +
    `export const LOCAL_UPLOADED_IMAGE_VERSION_BY_FILE = ${JSON.stringify(versions, null, 2)} as const;\n`;

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, content, 'utf8');

  // eslint-disable-next-line no-console
  console.log(`[generate-public-image-manifest] Wrote ${path.relative(repoRoot, outputFile)} (${files.length} files)`);
}

main();
